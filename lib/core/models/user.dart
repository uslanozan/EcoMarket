// static classes
class User {
  final int id;
  final String name;
  final String surName;
  final String email;
  final String avatarUrl;
  final String phone;

  User({
    required this.id,
    required this.phone,
    required this.name,
    required this.surName,
    required this.email,
    required this.avatarUrl,
  });
}
